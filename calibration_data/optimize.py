import heapq
from collections import defaultdict


def build_graph(edge_list):
    graph = defaultdict(list)
    for (u, v), error in edge_list:
        graph[u].append((v, error))
        graph[v].append((u, error))  # undirected
    return graph


def build_node_errors(node_error_list):
    """Convert node error list to dictionary"""
    return {node: error for node, error in node_error_list}


def find_min_error_path_with_nodes(
    edge_list, node_error_list, start_node, target_length
):
    graph = build_graph(edge_list)
    node_errors = build_node_errors(node_error_list)

    # Initialize with starting node's error
    start_error = node_errors.get(start_node, 0.0)
    pq = [(start_error, start_node, 0, frozenset([start_node]), [start_node])]

    best_overall = float("inf")
    best_path = None

    while pq:
        error, node, length, visited, path = heapq.heappop(pq)

        if error >= best_overall:
            continue

        if length == target_length:
            if error < best_overall:
                best_overall = error
                best_path = path
            continue

        for neighbor, edge_error in graph[node]:
            if neighbor in visited:
                continue

            if length < target_length:
                # Add both edge error AND neighbor node error
                neighbor_node_error = node_errors.get(neighbor, 0.0)
                new_error = error + edge_error + neighbor_node_error
                new_visited = visited | {neighbor}
                new_path = path + [neighbor]
                heapq.heappush(
                    pq, (new_error, neighbor, length + 1, new_visited, new_path)
                )

    # Convert path to edges
    if best_path:
        edges = [[best_path[i], best_path[i + 1]] for i in range(len(best_path) - 1)]
        return best_overall, edges, best_path
    return None, None, None


